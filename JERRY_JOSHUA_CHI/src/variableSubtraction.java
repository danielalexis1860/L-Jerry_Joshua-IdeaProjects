public class variableSubtraction {
    public static void main(String[] args) {
        //SUBTRACTION with INTEGER int
        int w = 13;
        int s = 34;
        System.out.println(w - s);

        // SUBTRACTION with integer int;
        int t = 24;
        int u = 17;
        System.out.println(t-u);

        //SUBTRACTION with integer int;
        int p = 56;
        int y = 76;
        System.out.println(p-y);

        //SUBTRACTION with integer int;
        int j = 10000000;
        int c = 200;
        System.out.println(j-c);

        //SUBTRACTION with long and INTEGER int;
        long r = 10000000;
        int g = 200;
        System.out.println(r -g);





    }
}


